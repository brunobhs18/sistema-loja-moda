    public void readFornecedorView(){
        DefaultTableModel modelo = (DefaultTableModel) tblFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedorDAO pdao = new FornecedorDAO();
        
        for(Fornecedor f: pdao.read()) {
            modelo.addRow(new Object[]{
            f.getNomefor(),
            f.getEnderecofor(),
            f.getContatofor()
        });
        }
        
    } 
    
    public void readFornecedorViewForBusca(String bus){
        DefaultTableModel modelo = (DefaultTableModel) tblFornecedor.getModel();
        modelo.setNumRows(0);
        FornecedorDAO pdao = new FornecedorDAO();
        
        for(Fornecedor f: pdao.readBusca(bus)) {
            modelo.addRow(new Object[]{
            f.getNomefor(),
            f.getEnderecofor(),
            f.getContatofor()
        });
        }
        
    } 
            
