    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblFornecedor.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Favor selecionar uma linha valida!");
            } else {
            DefaultTableModel tabelaFornecedor = (DefaultTableModel) tblFornecedor.getModel();
            int i = JOptionPane.showConfirmDialog(this, "Deseja excluir essa linha?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if(i == 0){
                Fornecedor f = new Fornecedor();
                FornecedorDAO dao = new FornecedorDAO();
                f.setNomefor(txtNomeFor.getText());
                dao.delete(f);
                
                txtNomeFor.setText("");
                txtEndereco.setText("");
                txtContato.setText("");   
        
                readFornecedorView();
            
            JOptionPane.showMessageDialog(this, "Excluido com sucesso!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tblFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFornecedorMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tblFornecedor.getSelectedRow();
        if (linhaSelecionada != -1){
           txtNomeFor.setText(tblFornecedor.getValueAt(tblFornecedor.getSelectedRow(), 0).toString());
            txtEndereco.setText(tblFornecedor.getValueAt(tblFornecedor.getSelectedRow(), 1).toString());
            txtContato.setText(tblFornecedor.getValueAt(tblFornecedor.getSelectedRow(), 2).toString());
          
        }else{
            txtNomeFor.setText("");
            txtEndereco.setText("");
            txtContato.setText("");
        }
        
    }//GEN-LAST:event_tblFornecedorMouseClicked

